public class DataTypes {
    public static void main(String[] args) {

        int n = 1234567890;
        // Long variables need to have an L at the end in order for the compiler to recognize it as a Long.
        long nL = 1234567895454455501L;

        //double uses 8 bytes
        double nD = 85.4561245;
        // float uses 4 bytes
        //float uses an F at the en of the digits in order for the compiler to know that it is a float.
        float nF = 123.456F;

    }
}
